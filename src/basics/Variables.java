package basics;

public class Variables {
    public static void main(String[] args) {
        // Declaration and Assignment
        int x = 4;
        System.out.println(x);
        // Changing variable
        x = 23;
        System.out.println(x+3);
        // String type
        String word = "TEST";
        System.out.println(word);
        // Concatenate Strings
        System.out.println(word + " 123");
    }
}
